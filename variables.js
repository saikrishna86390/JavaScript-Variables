// using let for changing values//
// In let the value will been updated we cant repeat variable name
let nameStudent = "saikrishna";
nameStudent = "chandu";
console.log(nameStudent);

//using const for changing values
//In const we cant update value and variable
const color = "red";
console.log(color);

//using var
//here we can update variable and values
var village = "khammam";
var village = "nellore";
console.log(village);
